package package3;

public class PostPrefix {
			
	public static void main(String[] args) {
		
		int x=10;
		int y=20;
		
		int a = x++; //Assignment then increment
		int b = ++y; //Increment then assignment
		
		System.out.println("Value of a "+a);
		System.out.println("Value of b "+b);
	}
	
}
